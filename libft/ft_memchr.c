#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t	i;
	char	*str;

	i = 0;
	str = (char *)s;
	while (i < n && str[i] != (char)c)
		i++;
	if (i == n)
		return (NULL);
	return ((void *)s + i);
}
